#!/bin/bash
set -e

# Check if there are changes to content directories
if [[ -z $(git status -s src/content/talks/ src/content/guides/ src/content/blog/) ]]; then
  echo "✓ No changes to featured content"
  exit 0
fi

# Create branch name with timestamp
BRANCH_NAME="update-featured-$(date +%Y%m%d-%H%M%S)"
PR_TITLE="chore: update featured content"

echo "📝 Creating branch $BRANCH_NAME"
git checkout -b "$BRANCH_NAME"

# Add content directory changes
git add src/content/talks/ src/content/guides/ src/content/blog/

# Commit changes
git commit -m "$PR_TITLE

Automated biweekly update of featured content flags.
Curated by GitHub Copilot CLI (claude-haiku-4.5).

🤖 Generated by GitHub Actions"

# Push branch
if ! git push origin "$BRANCH_NAME"; then
  echo "❌ Failed to push branch"
  exit 1
fi

echo "✓ Branch pushed successfully"

# Get change summary
CHANGE_SUMMARY=$(git diff main --stat src/content/talks/ src/content/guides/ src/content/blog/ 2>/dev/null || echo "Changes to featured content")

# Read the AI reasoning if it exists
REASONING=""
if [[ -f /tmp/featured-reasoning.md ]]; then
  REASONING=$(cat /tmp/featured-reasoning.md)
fi

# Build PR body in a temp file to avoid shell quoting issues
PR_BODY_FILE=$(mktemp)
cat > "$PR_BODY_FILE" <<EOF
Automated biweekly update of featured content flags, curated by GitHub Copilot CLI using Claude Haiku 4.5.

## Changes
\`\`\`
$CHANGE_SUMMARY
\`\`\`

## Curation Reasoning
$REASONING

---

**Review guide:** Check that the selected items align with the site positioning (Zero Trust, MCP, agentic AI). Adjust featured flags as needed before merging. This PR is NOT auto-merged.

Generated by GitHub Actions
EOF

# Create PR using GitHub CLI
echo "📬 Creating pull request"
gh pr create \
  --head "$BRANCH_NAME" \
  --title "$PR_TITLE" \
  --body-file "$PR_BODY_FILE"

PR_EXIT=$?
rm -f "$PR_BODY_FILE"

if [ $PR_EXIT -eq 0 ]; then
  echo "✅ Pull request created successfully (review and merge manually)"
else
  echo "❌ Failed to create pull request"
  exit 1
fi
